     */
    public function index()
    {
        $homes = homes::all();
        foreach($homes as $home){
            $pagination = $home->numbering;
            $mstread = $home->mostread;
        }

        $data = Home::where('deleted','=',0)->orderBy('id','desc')->paginate($pagination);
        $mostread = Home::orderBy('view','desc')->where('deleted','=',0)->take($mstread)->get();
        return view('frontend.beranda',compact('data','mostread'));
    }

